const myVariable = "Mathematics";

// the length property
/**returns the length if the string variable */

console.log(myVariable.length); // 11

// the string methods
/* 1. CHARACTER AT USING .charAt(): returns the value
at the provided index*/

console.log(myVariable.charAt(6)); //

// 2. INDEX OF()
/*returns the character at the provided index. if more 
than one character is provided it returns the first 
instance of the occurence. */
console.log(myVariable.indexOf("at")); // 1

// 3. LAST INDEX OF()
// the exact opposite of indexOf.
console.log(myVariable.lastIndexOf("at")); // 10
